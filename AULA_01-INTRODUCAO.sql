# Relatório

CREATE VIEW VW_RELATORIO AS SELECT VE.DATA, VE.CPF_CLIENTE, VE.QUANTIDADE,
	CLI.NOME AS 'CLIENTE', PRO.NOME AS 'PRODUTO', PRO.VALOR, (PRO.VALOR * VE.QUANTIDADE) AS 'TOTAL'
FROM VENDA VE
INNER JOIN CLIENTE CLI ON CLI.CPF = VE.CPF_CLIENTE # cria 'apelido' para cada coluna como 'cli', 've'
INNER JOIN PRODUTO PRO ON PRO.ID = VE.ID_PRODUTO # retorna o join 

SELECT * FROM VW_RELATORIO;

