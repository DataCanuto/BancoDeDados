#ddl Linguagem de Definição de Dados

DROP DATABASE aula00;
 
CREATE DATABASE aula00;

USE aula00;

CREATE TABLE cliente(
	CPF VARCHAR(14) NOT NULL,
    NOME VARCHAR(100) NOT NULL,
    IDADE INT NULL DEFAULT 18,
    PRIMARY KEY (CPF)
);

CREATE TABLE produto(
	ID INT NOT NULL auto_increment, # INTEIRO, NÃO NULO, AUTO INCREMENTO
    NOME VARCHAR(100) NOT NULL,
    QUANTIDADE INT NOT NULL,
    VALOR NUMERIC(9,2) NULL, # 9 digitos, 2 casas decimais 
    PRIMARY KEY(ID)
);

CREATE TABLE VENDA(
	ID INT NOT NULL AUTO_INCREMENT,
    CPF_CLIENTE VARCHAR(14) NOT NULL,
    ID_PRODUTO INT NOT NULL,
    DATA DATETIME NULL,
    QUANTIDADE INT NOT NULL,
    PRIMARY KEY(ID),
    CONSTRAINT FK_VENDACLIENTE FOREIGN KEY(CPF_CLIENTE) REFERENCES cliente(CPF), 
    # 'constraint': palavra reservada para dar nome a regra
    CONSTRAINT FK_VENDAPRODUTO FOREIGN KEY(ID_PRODUTO) REFERENCES produto(ID)
);

